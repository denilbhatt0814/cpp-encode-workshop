#include <iostream>
#include <bits/stdc++.h>
// #include <cmath>

using namespace std;

int main() {
  // pow(a,x) -> a^x
  cout << pow(2,5) << endl;
  
  // for sqrt
  cout << pow(2.0,0.5)<<endl;
  //or (but pow is better than sqrt)
  cout << sqrt(2) << endl;

  // Ceil and floor
  cout << ceil(1.5)<<endl;
  cout<< floor(1.5) << endl;
} 
